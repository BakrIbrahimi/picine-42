int	ft_str_is_lowercase(char	*str)
{
	int		m;
	int		ret;
	char	f;

	m = 0;
	ret = 1;
	while (*(str + m) != '\0')
	{
		f = *(str + m);
		if (f < 97 || f > 122)
		{
			ret = 0;
		}
		m++;
	}
	return (ret);
}
