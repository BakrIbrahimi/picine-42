int	ft_str_is_printable(char	*str)
{
	int		a;
	int		ret;
	char	b;

	a = 0;
	ret = 1;
	while (*(str + a) != '\0')
	{
		b = *(str + a);
		if (b <= 31 || b == 127)
		{
			ret = 0;
		}
		a++;
	}
	return (ret);
}
