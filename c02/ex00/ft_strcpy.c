#include <unistd.h>
char	*ft_strcpy(char	*dest, char	*src)
{
	int	ba;

	ba = 0;
	while (*(src + ba) != '\0')
	{
		*(dest + ba) = *(src + ba);
		ba++;
	}
	dest[ba] = '\0';
	return (dest);
}
