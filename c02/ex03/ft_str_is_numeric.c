int	ft_str_is_numeric(char	*str)
{
	int		s;
	int		ret;
	char	n;

	s = 0;
	ret = 1;
	while (*(str + s) != '\0')
	{
		n = *(str + s);
		if (n < 48 || n > 57)
		{
			ret = 0;
		}
		s++;
	}
	return (ret);
}
