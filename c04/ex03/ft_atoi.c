int	ft_atoi(char	*str)
{
	int	m;
	int	res;
	int	neg;

	m = 0;
	res = 1;
	neg = 0;
	while (str[m] == ' ' || str[m] == '\f' || str[m] == '\n'
		|| str[m] == '\r' || str[m] == '\t' || str[m] == '\v')
	{
		m++;
	}
	while (str[m] == '-' || str[m] == '+')
	{
		if (str[m] == '-')
			res *= -1;
		m++;
	}
	while (str[m] >= '0' && str[m] <= '9')
	{
		neg = ((neg * 10) + str[m] - '0');
		m++;
	}
	neg *= res;
	return (neg);
}
