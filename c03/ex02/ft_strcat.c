char	*ft_strcat(char	*dest, char	*src)
{
	char	*ba;
	char	*ib;

	ba = dest;
	ib = src;
	while (*ba)
		ba++;
	while (*ib)
	{
		*ba = *ib;
		ib++;
		ba++;
	}
	*ba = 0;
	return (dest);
}
